DECLARE
    @ANIVERSARIO TABLE(
        NOME VARCHAR(50),
        ANIVERSARIO DATE
    )
 
BEGIN
 
    INSERT INTO @ANIVERSARIO VALUES
        ('PESSOA1','1978-08-28'),
        ('PESSOA2','1980-03-04'),
        ('PESSOA3','1982-01-11'),
        ('PESSOA4','1984-06-01'),
        ('PESSOA5','1950-12-07')
 
    SELECT NOME
         , ANIVERSARIO
         , YEAR(GETDATE())*10000+MONTH(ANIVERSARIO)*100+DAY(ANIVERSARIO)  AS DTREF
      FROM @ANIVERSARIO
    WHERE YEAR(GETDATE())*10000+MONTH(ANIVERSARIO)*100+DAY(ANIVERSARIO) > CONVERT(CHAR(8),GETDATE(),112)
    ORDER BY 3
 
END